package com.cng.booking;

public class GetterDemo
{
	 static String name ;
	 static String email;
	 static String city;
	 static String mobile;
	 static String pname;
	 static int pid;
	 static String ucity;
	
	public static String getUcity() {
		return ucity;
	}
	public static void setUcity(String ucity) {
		GetterDemo.ucity = ucity;
	}
	public static int getPid() {
		return pid;
	}
	public static void setPid(int pid) {
		GetterDemo.pid = pid;
	}
	public static String getPname() {
		return pname;
	}
	public static void setPname(String pname) {
		GetterDemo.pname = pname;
	}
	public static String getMobile() {
		return mobile;
	}
	public static void setMobile(String mobile) {
		GetterDemo.mobile = mobile;
	}
	public static String getName() {
		return name;
	}
	public static void setName(String name) {
		GetterDemo.name = name;
	}
	public static String getEmail() {
		return email;
	}
	public static void setEmail(String email) {
		GetterDemo.email = email;
	}
	public static String getCity() {
		return city;
	}
	public static void setCity(String city) {
		GetterDemo.city = city;
	}

	

}
